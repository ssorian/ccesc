// --- Main Component ---

interface User {
  name: string
  email: string
  avatar?: string
}

interface NavItem {
  title: string
  url: string
  icon?: LucideIcon
  isActive?: boolean
  items?: {
    title: string
    url: string
  }[]
}

interface Project {
  name: string
  url: string
  icon: LucideIcon
}

interface AppSidebarProps extends React.ComponentProps<typeof Sidebar> {
  user: User
  navMain: NavItem[]
  projects?: Project[]
}

export function AppSidebar({ user, navMain, projects, ...props }: AppSidebarProps) {
  return (
    <Sidebar collapsible="icon" {...props}>
      <SidebarHeader>
        <NavUser user={user} />
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={navMain} />
        {projects && <NavProjects projects={projects} />}
      </SidebarContent>
      <SidebarRail />
    </Sidebar>
  )
}

